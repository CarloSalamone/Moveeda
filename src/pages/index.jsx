import styles from "../styles/Home.module.scss";

import { useEffect, useState } from "react";
import { getSession } from "next-auth/react";
import { HTTP_GET } from "../../libs/HTTP";

import Head from "next/head";
import Header from "@/components/header";
import Slider from "@/components/slider";
import TabButton from "../components/tabButton";
import EventList from "../components/eventList";
import NavBar from "@/components/navBar";

const Home = ({ session }) => {
  const [selectedTab, setSelectedTab] = useState("Museec");
  const [categories, setCategories] = useState([]);
  const [events, setEvents] = useState([]);

  useEffect(() => {
    const getCategories = async () => {
      const events = await HTTP_GET(`events/search?category=${selectedTab}`);
      setEvents(events);
    };
    getCategories();
  }, [selectedTab]);

  useEffect(() => {
    const getCategories = async () => {
      const categories = await HTTP_GET("categories");
      setCategories(categories);
    };
    getCategories();
  }, []);

  const handleSelect = (selectedButton) => {
    setSelectedTab(selectedButton);
  };

  return (
    <>
      <Head>
        <title>Moveeda</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <Header />
        <Slider />
        <section className={styles.SectionTab}>
          {categories.map((category, key) => (
            <TabButton
              key={category._id}
              color={category.color}
              onSelect={() => handleSelect(category.name)}
            >
              {category.name.toUpperCase()}
            </TabButton>
          ))}
        </section>
        <EventList events={events} title={selectedTab} />
        <NavBar userType={session?.user?.type} userId={session?.users?.id} />
      </main>
    </>
  );
};

export async function getServerSideProps(context) {
  return {
    props: {
      session: await getSession(context),
    },
  };
}

export default Home;
